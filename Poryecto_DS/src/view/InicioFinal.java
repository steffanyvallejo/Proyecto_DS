    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = jTextField1.getText();
        String pass = jPasswordField1.getText();
        cn = new ConexionDBM();
        Empleado emp = cn.login(user, pass);
        if(emp==null){
            jLabel4.setText("Usuario o contraseña inválido");
            jLabel4.setVisible(true);
        }else{
            String t = emp.getTipo();
            if(t.equals("v")){
                Vendedor v = (Vendedor)emp;     
                VendedorFinal ventanaV = new VendedorFinal(v);
                ventanaV.jLabel2.setText(v.getNombre()+" "+v.getApellido());
                this.setVisible(false);
                ventanaV.setVisible(true);
            }else if(t.equals("g")){
                Gerente g = (Gerente)emp;                     
                GerenteFinal ventanaG = new GerenteFinal(g);
                ventanaG.jLabel2.setText(g.getNombre()+" "+g.getApellido());
                this.setVisible(false);
                ventanaG.setVisible(true);
            }else if(t.equals("a")){
                Administrador a = (Administrador)emp;                                   
                AdministradorFinal ventanaA = new AdministradorFinal(a);
                ventanaA.jLabel2.setText(a.getNombre()+" "+a.getApellido());                
                this.setVisible(false);
                ventanaA.setVisible(true);                
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed
            
